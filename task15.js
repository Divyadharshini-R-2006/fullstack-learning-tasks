const day="Thu";
switch(day){
    case "Mon":
        console.log("Today is Monday");
        break;
    case "Tue":
        console.log("Today is Tuesday");
        break;
    case "Wed":
        console.log("Today is Wednesday");
        break;
    case "Thu":
        console.log("Today is Thursday");
        break;
    default:
        console.log("Wow what a wonderful day....!");
}